async def sudo():
    global SUDOERS
    SUDOERS.add(config.SUDO_USERS)
    sudoersdb = mongodb.sudoers
    sudoers = await sudoersdb.find_one({"sudo": "sudo"})
    sudoers = [] if not sudoers else sudoers["sudoers"]
    if config.SUDO_USERS not in sudoers:
        sudoers.append(config.SUDO_USERS)
        await sudoersdb.update_one(
            {"sudo": "sudo"},
            {"$set": {"sudoers": sudoers}},
            upsert=True,
        )
    if sudoers:
        for user_id in sudoers:
            SUDOERS.add(user_id)
    LOGGER(__name__).info(f"Loaded Sudo Users Successfully.")


def heroku():
    global HAPP
    if is_heroku:
        if config.HEROKU_API_KEY and config.HEROKU_APP_NAME:
            try:
                Heroku = heroku3.from_key(config.HEROKU_API_KEY)
                HAPP = Heroku.app(config.HEROKU_APP_NAME)
                LOGGER(__name__).info(f"Heroku app configured")
            except BaseException:
                LOGGER(__name__).warning(
                    f"Please make sure your HEROKU_API_KEY and HEROKU_APP_NAME are configured correctly in the heroku."
                )
